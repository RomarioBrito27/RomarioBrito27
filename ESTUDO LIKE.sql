WHERE Identificacao LIKE '%RO'

--SE O SIMBOLO DE % FOR NO INICIO E NO FINAL RETORNARA TUDO QUE FOR ENTRE OS DOIS SIMBOLOS ANTES OU DEPOIS EXEMPLO 

