-- COMANDO LIKE  SERVE PARA CONSEGUIR ENCONTRAR MAIS RÁPIDO ALGO QUE NÃO LEMBRE ENTÃO ELE FILTRA O DADOS USANDO O LIKE '....%  EXEMPLO 

SELECT * FROM Operadors
WHERE Identificacao LIKE 'NE%'

--SE O SIMBOLO DE % FOR  NO FINAL ELE BUSCA TUDO QUE VEM ANTES DO SIMBOLO SE FOR NO INICIO BUSCA TUDO QUE FOR DEPOIS DO SIMBOLO EXEMPLO 

